#creating the add function
def add(a,b):
    return a + b 

#creating substract function
def substract(a,b):
    return a-b
#creating divide function
def divide(a,b):
    return a/b
#printing output of all three function calls
print(add(1,2))
print(substract(4,1))
print(divide(8,2))